				<h2>What is Hybrid Intelligence?</h2>
				<p>Hybrid Intelligence is an umbrella term that refers to creating software systems that exhibit ability to learn, adapt, understand context, and work seamlessly with humans and for humans. Core areas that are part of Hybrid Intelligence include artificial intelligence, machine learning, human-computer interaction, cognitive sciences, natural language processing, computer vision, data science, information retrieval, and large databases. Hybrid Intelligence has connections and is informed by research in behavioral sciences, cognitive neuroscience, data science, big data, algorithms, programming languages, information systems, and design. While Hybrid Intelligence deals with theoretical questions surrounding ability of machines to learn and exhibit intelligence, it is fundamentally an interdisciplinary area that is driven by a wide range of real life challenges at the interface between humans and machines.</p>
